def A(n)
  m = Math.sqrt(n).to_i + 1
  h = {}
  (1..m).each{|i|
    (1..m).each{|j|
      k = i * i + 7 * j * j
      break if k > n
      if h.key?(k)
        h[k] += 1
      else
        h[k] = 1
      end
    }
  }
  h.sort
end

