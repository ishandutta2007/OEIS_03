def ncr(n, r)
  return 1 if r == 0
  (n - r + 1..n).inject(:*) / (1..r).inject(:*)
end
def A(k, n)
  (0..n).map{|i| (0..k).inject(0){|s, j| s + ncr(2 * k, 2 * j) * (i + 1) ** (k - j) * i ** j}}
end
def A322699(n)
  a = []
  (0..n).each{|i| a << A(i, n - i)}
  ary = []
  (0..n).each{|i|
    (0..i).each{|j|
      ary << a[i - j][j]
    }
  }
  ary
end
